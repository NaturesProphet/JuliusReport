    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!PostoF.getText().isEmpty()) {
            Posto posto = new Posto();
            PostoDAO dao = new PostoDAO();

            posto.setNome(PostoF.getText());
            if (!LocalF.getText().isEmpty()) {
                posto.setLocal(LocalF.getText());
            }
            try {
                dao.salvar(posto);
                JOptionPane.showMessageDialog(null, "Posto Registrado com Sucesso.");
                dispose();
            } catch (SQLException ex) {
                Logger.getLogger(CadastrarPosto.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro ao salvar\n" + ex);
            } catch (IOException ex) {
                Logger.getLogger(CadastrarPosto.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Erro de entrada e saida\n" + ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Preencha pelomenos "
                    + "um nome para o posto");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

